# ── Core query functions ──────────────────────────────────────────────────────


def load_graph(paths: list[Path]) -> rdflib.Graph:
    """Load one or more RDF files into a single merged in-memory graph."""
    g = rdflib.Graph()
    for p in paths:
        g.parse(str(p), format=_detect_format(p))
    return g


def run_query(paths: list[Path], sparql_text: str) -> QueryResult:
    """Execute *sparql_text* against the taxonomy files at *paths*.

    Primary entry-point.  AI callers should generate a SPARQL string and
    pass it here — the result shape is always a ``QueryResult``.
    """
    if not sparql_text.strip():
        return QueryResult(columns=[], rows=[], error="Empty query.")
    try:
        g = load_graph(paths)
    except Exception as exc:
        return QueryResult(columns=[], rows=[], error=f"Load error: {exc}")
    return run_query_on_graph(g, sparql_text)


def run_query_on_graph(g: rdflib.Graph, sparql_text: str) -> QueryResult:
    """Execute *sparql_text* against an already-loaded *g*.

    Separate from *run_query* so callers that maintain a cached or augmented
    graph (e.g. with a reasoner) can skip the file-load step.
    """
    try:
        result = g.query(sparql_text)
    except Exception as exc:
        return QueryResult(columns=[], rows=[], error=f"Query error: {exc}")

    qtype = _detect_query_type(sparql_text)

    if qtype == "ASK":
        return QueryResult(
            columns=["result"],
            rows=[["true" if bool(result) else "false"]],
            query_type="ASK",
        )

    if qtype in ("CONSTRUCT", "DESCRIBE"):
        g2 = getattr(result, "graph", None)
        ttl = g2.serialize(format="turtle") if g2 is not None else ""
        lines = [ln for ln in ttl.splitlines() if ln.strip()]
        return QueryResult(columns=["triple"], rows=[[ln] for ln in lines], query_type=qtype)

    # SELECT
    cols: list[str] = [str(v) for v in result.vars] if result.vars else []
    rows: list[list[str]] = []
    for row in result:
        rows.append([_fmt_value(row[i]) for i in range(len(cols))])  # type: ignore[index]
    return QueryResult(columns=cols, rows=rows, query_type="SELECT")


def find_uri_column(result: QueryResult) -> int | None:
    """Return the index of the first column whose values look like concept URIs.

    Used by the TUI to offer direct tree-navigation from a result row.
    Returns ``None`` when no URI column is detected.
    """
    if not result.rows:
        return None
    for col_idx in range(len(result.columns)):
        sample = [row[col_idx] for row in result.rows[:10] if col_idx < len(row)]
        uri_hits = sum(
            1
            for v in sample
            if v.startswith("http://") or v.startswith("https://") or v.startswith("urn:")
        )
        if uri_hits >= max(1, len(sample) // 2):
            return col_idx
    return None


def compute_col_widths(columns: list[str], rows: list[list[str]], available: int) -> list[int]:
    """Compute column display widths to fill *available* characters.

    Each column gets at least 4 characters.  Widths are proportional to the
    maximum cell length (capped at 40) so no single column dominates.
    """
    n = len(columns)
    if n == 0:
        return []
    maxes: list[int] = [max(4, len(c)) for c in columns]
    for row in rows[:100]:
        for i, val in enumerate(row):
            if i < n:
                maxes[i] = max(maxes[i], len(val))
    maxes = [min(m, 40) for m in maxes]
    sep = n - 1  # " │ " is 3 chars but we'll use 1-char sep
    total_max = sum(maxes)
    budget = max(n * 4, available - sep * 3)
    if total_max <= budget:
        return maxes
    scale = budget / total_max
    widths = [max(4, int(m * scale)) for m in maxes]
    # absorb rounding error in the last column
    diff = budget - sum(widths)
    widths[-1] = max(4, widths[-1] + diff)
    return widths


# ── Internal helpers ──────────────────────────────────────────────────────────


def _fmt_value(val: object) -> str:
    if val is None:
        return ""
    if isinstance(val, rdflib.URIRef):
        return str(val)
    if isinstance(val, rdflib.Literal):
        lang = getattr(val, "language", None)
        return f"{val}@{lang}" if lang else str(val)
    return str(val)


def _detect_format(path: Path) -> str:
    return {
        ".ttl": "turtle",
        ".n3": "n3",
        ".jsonld": "json-ld",
        ".rdf": "xml",
        ".owl": "xml",
    }.get(path.suffix.lower(), "turtle")


def _detect_query_type(sparql_text: str) -> str:
    m = re.search(r"\b(SELECT|ASK|CONSTRUCT|DESCRIBE)\b", sparql_text, re.IGNORECASE)
    return m.group(1).upper() if m else "SELECT"
