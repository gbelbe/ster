"""SPARQL query engine for ster.

All query logic is isolated here so that the TUI, CLI commands, and future
AI-powered callers all share the same entry-points.

Extension points
────────────────
• *run_query* / *run_query_on_graph* — pass any SPARQL string, including
  one generated by an AI, and receive a uniform ``QueryResult``.
• *PRESET_QUERIES* — a registry of built-in queries; extend this list to
  add domain-specific or AI-suggested queries to the preset menu.
• *find_uri_column* — heuristic to detect which result column contains
  concept URIs so the TUI can offer tree-navigation from results.
"""

from __future__ import annotations

import re
from dataclasses import dataclass
from pathlib import Path

import rdflib

# ── Public data types ─────────────────────────────────────────────────────────


@dataclass
class QueryResult:
    """Normalised result from any SPARQL query type."""

    columns: list[str]
    rows: list[list[str]]  # every cell is a plain string
    error: str = ""
    query_type: str = "SELECT"  # SELECT | ASK | CONSTRUCT | DESCRIBE


@dataclass
class PresetQuery:
    label: str
    description: str
    sparql: str


